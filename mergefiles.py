EXTRACT_FILENAME_REGEX = re.compile("^.*/(.*)_[0-9a-f]+\.s$")


def extract_filename(name: str) -> str:
    match = EXTRACT_FILENAME_REGEX.match(name)
    if match is None:
        raise ValueError(f"{name} is not a valid filename")
    return match.group(1)


for f in text_files:
    src_files = [f]
    filename = extract_filename(f)
    for section in [
            ctors_files, rodata_files, data_files, bss_files, sdata_files,
            sbss_files, sbss2_files
    ]:
        if len(section) == 0:
            continue
        if extract_filename(section[0]) != filename:
            continue
        src_files.append(section.popleft())
    file_list_str = ' '.join(src_files)
    print(f"cat {file_list_str} > {f}.new")
    print(f"rm {file_list_str}")
    print(f"mv {f}.new {f}")
